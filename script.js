  function frase() {
    const elemento = document.getElementById("historia");
    elemento.classList.add('fade-out'); 
    
    setTimeout(() => {
        const num = Math.floor(Math.random() * frases.length);
        elemento.innerText = frases[num];
        elemento.classList.remove('fade-out'); 
    }, 500); 
}

